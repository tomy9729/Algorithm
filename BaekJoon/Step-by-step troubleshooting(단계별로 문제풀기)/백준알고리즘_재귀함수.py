def star(n) :
  if n == 3 : 
    return [['*','*','*'],['*',' ','*'],['*','*','*']]
  arr = [[0]*n for i in range(n)]
  temp = star(n//3)
  for i in range(n) : 
    for j in range(n) : 
      if i >= n//3 and i < 2*n//3 and j >= n//3 and j < 2*n//3 : 
        arr[i][j] = ' '
      else :
        arr[i][j] = temp[i%(n//3)][j%(n//3)]
  return arr

n=int(input())
x = star(n)
for i in range(n):
    for j in range(n):
        print(x[i][j],end='')
    print(end='\n')

